core:add_listener(
    "rhox_iee_nor_monster_hunt_mission_success",
    "MissionSucceeded",
    function(context)
        local mission_key = context:mission():mission_record_key()
        return rhox_iee_nor_factions[context:faction():name()] and rhox_nor_final_mission[mission_key]
    end,
    function(context)
        local faction = context:faction()
        local faction_key = faction:name()
        local key = context:mission():mission_record_key()
        local monster_hunt_data_for_faction = rhox_nor_monster_hunts[faction_key]


        for i = 1, #monster_hunts_index do
            if key == monster_hunt_data_for_faction[monster_hunts_index[i]]["qb"] then
                if not cm:get_saved_value("norscan_favour_lvl_3_reached_" .. faction_key) then
                    if faction_key == "rhox_nor_firebrand_slavers" then
                        cm:faction_add_pooled_resource(faction_key, "nor_progress_hound", "events", 12)
                    elseif faction_key == "rhox_nor_ravenblessed" then
                        cm:faction_add_pooled_resource(faction_key, "nor_progress_eagle", "events", 12)
                    else
                        cm:trigger_dilemma(faction_key, monster_hunts[faction_key][monster_hunts_index[i]]["fin"]);
                    end
                end;
                
                deactivate_a_monster_hunt(monster_hunts_index[i], faction_key);
                
                if key == "wh_dlc08_qb_nor_monster_hunt_6" then
                    cm:remove_event_restricted_unit_record_for_faction("wh_dlc08_nor_mon_war_mammoth_ror_1", faction_key);
                elseif key == "wh_dlc08_qb_nor_monster_hunt_0" then
                    cm:remove_event_restricted_unit_record_for_faction("wh_dlc08_nor_mon_frost_wyrm_ror_0", faction_key);
                end;
            end;
        end;



    end,
    true
)

